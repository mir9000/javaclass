package org.example.class7;

public class E5ArrayWhileloop {
    public static void main(String[] args) {
        String[] names = {"Lana", "Wais", "Matt", "Maggy", "Noodles", "James"};

        int i = 0;
        while (i <= 5) {///while loop
            System.out.println(names[i]);
            i++;


        }
    }
}

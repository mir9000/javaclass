package org.example.class7;

public class E11ArrayAdditionTask {
    public static void main(String[] args) {

        //adding all the numbers inside the array

        int[] numbers={10,20,30,80,40,50};

        int sum=0;

        for(int num:numbers){


            sum=sum+num;
        }
        System.out.println(sum);


    }
}

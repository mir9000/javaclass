package org.example.class3;

public class E6Short {
    public static void main(String[] args) {
        int number=100;
        // number=number*10;    the shorter way is
        number*=10;  // multiply 10 to the value of number variable
        System.out.println(number);


        int number1=100;
        // number=number/10;    the shorter way is
        number1/=10;  // divide 10 to the value of number variable
        System.out.println(number1);

        int number2=100;
        // number=number%10;    the shorter way is
        number2%=10;  // remainder 10 to the value of number variable
        System.out.println(number2);
    }
    }



